#include <iostream>
# include <cmath> // 수학 관련 기능을 제공해주는 library

using namespace std;

int main() {
   double result = floor(1.2);
   cout << result << endl; // 1

   double powered = pow(2, 3);
   cout << powered; // 8
   return 0;
}

